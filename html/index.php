<div align="center"><center><h3>&nbsp;</h3></center><h2>The Sloan Digital Sky Survey</h2></div>
<p>The Sloan Digital Sky Survey (SDSS) is one of the most ambitious and influential surveys in the history of astronomy. Over eight years of operations (SDSS-I, 2000-2005; SDSS-II, 2005-2008), it obtained deep, multi-color images covering more than a quarter of the sky and created 3-dimensional maps containing more than 930,000 galaxies and more than 120,000 quasars. </p>
<p>SDSS data have been released to the scientific community and the general public in annual increments, with the final public data release from SDSS-II occurring in October 2008. That release, <a href="dr7/">Data Release 7</a>, is available through this website.</p>
<p>Meanwhile, SDSS is continuing with the <a href="http://www.sdss3.org">Third Sloan Digital Sky Survey (SDSS-III)</a>, a program of four new 
surveys using SDSS facilities. SDSS-III began observations in July 2008 and released <a href="http://www.sdss3.org/dr8">Data Release 8</a> in January 2011, <a href="http://www.sdss3.org/dr9">Data Release 9</a> in August 2012, and <a href="http://www.sdss3.org/dr10">Data Release 10</a> in July 2013.  SDSS-III will continue 
operating and releasing data through 2014.</p>

<p><a href="http://www.sdss3.org/dr9">Data Release 10</a> contains the first
release of APOGEE infrared Galactic spectroscopy as well as cumulative updates
to the BOSS optical extragalactic spectroscopy archive. </p>

<p><a href="http://www.sdss3.org/dr9">Data Release 9</a> contains the first
release of BOSS spectroscopy to the public as well as several significant
updates to the cumulative SDSS archive.</p>
<p><a href="http://www.sdss3.org/dr8">Data Release 8</a> contains all images from the&nbsp;SDSS telescope - <a href="http://www.sdss3.org/press/20110111.largestimage.php">the largest color image of the sky ever made</a>. It also includes measurements for nearly 500 million stars and galaxies, and spectra of nearly two million. All the images, measurements, and spectra are available free online. You can <a href="http://skyserver.sdss3.org/dr8/en/tools/chart/navi.asp">browse through sky images</a>, look up <a href="http://skyserver.sdss3.org/dr8/en/tools/quicklook/quickobj.asp">data for individual objects</a>, or <a href="http://skyserver.sdss3.org/dr8/en/tools/search/">search for objects</a> anywhere in the sky based on any criteria.</p>
<p>
The SDSS used a dedicated 2.5-meter telescope at Apache Point Observatory, New Mexico, 
equipped with two powerful special-purpose instruments. 
The 120-megapixel camera imaged 1.5 square degrees of sky at a time, 
about eight times the area of the full moon. A pair of spectrographs 
fed by optical fibers measured spectra of (and hence distances to) 
more than 600 galaxies and quasars in a single observation. 
A custom-designed set of software pipelines kept pace with the 
enormous data flow from the telescope. The two key technologies that enabled the SDSS, optical fibers and the digital imaging detectors known as CCDs, were the discoveries awarded the <a href="http://nobelprize.org/nobel_prizes/physics/laureates/2009/index.html">2009 Nobel Prize in Physics.</a>
</p>
<p>
During its first phase of operations, 2000-2005, the SDSS imaged
more than 8,000 square degrees of the sky in five optical bandpasses,
and it obtained spectra of galaxies and quasars selected from 5,700
square degrees of that imaging.  It also obtained repeated imaging
(roughly 30 scans) of a 300 square degree stripe in the 
southern Galactic cap.

<p>
With new financial support and an expanded collaboration including
25 institutions around the globe, SDSS-II carried out three distinct
surveys:<br />

<ul class="pad">
<li><a href="legacy/index.html">
   The Sloan Legacy Survey</a>
   completed the original SDSS imaging and
   spectroscopic goals. The final dataset includes 230 million celestial
   objects detected in 8,400 square degrees of imaging and spectra of
   930,000 galaxies, 120,000 quasars, and 225,000 stars.</li>
<li><a href="segue/index.html">
   SEGUE</a> (the Sloan Extension for Galactic Understanding and Exploration)
   probed the structure and history of the Milky Way galaxy,
   with new imaging of 3500 square degrees and spectra of
   240,000 stars in a variety of categories in selected fields.</li>
<li><a href="supernova/aboutsupernova.html">
   The Sloan Supernova Survey </a>
   carried out repeat imaging of the 300 square degree southern
   equatorial stripe to discover and measure supernovae and other
   variable objects. In the course of three 3-month campaigns,
   the supernova survey discovered nearly 500 spectroscopically
   confirmed Type Ia supernovae, which are being used to
   determine the history of the accelerating cosmic expansion over
   the last 4 billion years.</li>
</ul>

<p>
SDSS data have supported fundamental work across an extraordinary
range of astronomical disciplines, including the properties of galaxies,
the evolution of quasars, the structure and stellar populations
of the Milky Way, the dwarf galaxy companions of the Milky Way
and M31, asteroids and other small bodies in the solar system,
and the large scale structure and matter and energy contents of the universe; 
this site includes <a href="signature.html">For a brief overview of SDSS 
science contributions</a>. A more in-depth view can be found in the online materials from 
the 2008 Symposium
<a href="http://sdss2008.uchicago.edu/program.html">
The Sloan Digital Sky Survey: Asteroids to Cosmology.
</a>
</p>

